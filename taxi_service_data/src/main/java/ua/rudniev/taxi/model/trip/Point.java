package ua.rudniev.taxi.model.trip;

/**
 * This class has fields and methods for coordinates (x, y)
 */
public class Point {
    private final double x;
    private final double y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
